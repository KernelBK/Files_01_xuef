
##hash-table
"""
we need some way to look up a value given a variable’s name.
Later, when we add classes, we’ll also need a way to store fields on instances.
The perfect data structure for these problems and others is a hash table.
"""
##why useful
"""
Hash tables appear in so many languages because they are incredibly powerful.
Much of this power comes from one metric: given a key, a hash table returns the
corresponding value in constant time, regardless of how many keys are in the hash table.
"""
